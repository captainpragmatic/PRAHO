CATALOG_BY_KEY: dict[str, SettingDef] = {definition.key: definition for definition in CATALOG}

# Backward-compatible defaults map (SettingsService.DEFAULT_SETTINGS derives from this)
DEFAULTS: dict[str, Any] = {definition.key: definition.default for definition in CATALOG}

SENSITIVE_KEYS: frozenset[str] = frozenset(d.key for d in CATALOG if d.sensitive)
CRITICAL_KEYS: frozenset[str] = frozenset(d.key for d in CATALOG if d.critical)


def defs_for_group(slug: str) -> list[SettingDef]:
    """Catalog entries for one group, in stable key order"""
    return [d for d in CATALOG if d.group == slug]


def groups_in_zone(zone: str) -> list[GroupDef]:
    """Groups belonging to one UI zone, in declaration order"""
    return [g for g in GROUPS if g.zone == zone]


def is_sensitive_key(key: str) -> bool:
    """Catalog-flag sensitivity; unknown keys fall back to name-pattern matching
    so ad-hoc keys with secret-shaped names are still encrypted at rest."""
    definition = CATALOG_BY_KEY.get(key)
    if definition is not None:
        return definition.sensitive
    lowered = key.lower()
    return any(pattern in lowered for pattern in ("password", "secret", "token", "credential"))


def validation_rules_for_key(key: str) -> dict[str, Any]:
    """Catalog-owned validation rules (the model's validation_rules field is retired)"""
    definition = CATALOG_BY_KEY.get(key)
    if definition is None or definition.validation is None:
        return {}
    return definition.validation
