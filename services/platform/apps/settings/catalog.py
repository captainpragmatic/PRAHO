"""
Settings catalog — the single source of truth for runtime settings (ADR-0042).

Every runtime setting the platform exposes is declared here: key, type, default,
UI placement (zone → group → section), input kind, sensitivity, criticality, and
validation rules. The database stores current values; this catalog owns identity
and metadata. A consumer-contract test enforces that every key declared here is
read somewhere in production code — decoy settings cannot exist.

The e-Factura entries mirror apps.billing.efactura.settings.EFACTURA_DEFAULTS
(kept as literals to avoid a settings → billing import cycle); a test asserts
the two stay identical.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import TYPE_CHECKING, Any

from django.utils.translation import gettext_lazy as _

if TYPE_CHECKING:
    from django.utils.functional import _StrPromise

    LazyLabel = str | _StrPromise
else:
    LazyLabel = str

# Zones: the three UI surfaces
ZONE_BUSINESS = "business"
ZONE_INTEGRATIONS = "integrations"
ZONE_PLATFORM = "platform"


@dataclass(frozen=True)
class GroupDef:
    """A navigable settings group (one page in the UI)"""

    slug: str
    zone: str
    label: LazyLabel
    description: LazyLabel = ""
    section_order: tuple[str, ...] = ()


@dataclass(frozen=True)
class SettingDef:
    """Declaration of one runtime setting"""

    key: str
    data_type: str  # string | integer | boolean | decimal | list | json
    default: Any
    group: str
    section: LazyLabel
    label: LazyLabel
    help_text: LazyLabel = ""
    unit: str = ""
    input_kind: str = "text"  # text | number | toggle | select | chips | json | secret
    sensitive: bool = False
    critical: bool = False
    requires_restart: bool = False
    advanced: bool = False
    validation: dict[str, Any] | None = field(default=None, hash=False)


